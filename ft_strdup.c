#include "libft.h"

char		*ft_strdup(const char *s)
{
	int		i;
	char	*tab;

	i = 0;
	tab = (char*)malloc(sizeof(char) * ft_strlen(s) + 1);
	if (tab == 0)
		return (NULL);
	while (s[i])
	{
		tab[i] = s[i];
		i++;
	}
	tab[i] = '\0';
	return (tab);
}
