#include "libft.h"

void		*ft_memset(void *s, int c, size_t n)
{
	char	*tab;
	size_t	i;

	i = 0;
	tab = (char*)s;
	while (i < n)
	{
		tab[i] = c;
		i++;
	}
	return (s);
}
